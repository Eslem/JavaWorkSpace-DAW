package com.fpmislata.series.dominio;

public class Series {
    
    private int idSerie;
    private String nombre;
    
    public Series(){
    }

    public int getIdserie() {
        return idSerie;
    }

    public void setIdserie(int idserie) {
        this.idSerie = idserie;
    }

    public String getNombre() {
        return nombre;
    }

    public void setNombre(String nombre) {
        this.nombre = nombre;
    }
    
    
}
